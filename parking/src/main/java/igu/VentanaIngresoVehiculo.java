    //Evento del boton guardar
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Obtener los datos ingresados en los textfields
        String patente = txtfPatente.getText();
        // Formatear la cadena de fecha y hora para coincidir con el formato esperado
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        LocalDateTime horaIngreso = LocalDateTime.parse(txtfHoraIngreso.getText(), formatter); // Convertir el texto a LocalDateTime
        String tipo = txtfTipo.getText();
        String color = txtfColor.getText();
        String marca = txtfMarca.getText();

        // Llamar al m√©todo GuardarDatosVehiculo de Parquimetro
        parquimetro.GuardarDatosVehiculo(patente, horaIngreso, tipo, color, marca);

        //imprimir datos para verificar que se estan guardando
        parquimetro.MostrarDatos2();

        // Cerrar la ventana actual
        dispose();
        
        //Abrir nueva ventana
        VentanaValorCobrar vValor = new VentanaValorCobrar();
        vValor.setVisible(rootPaneCheckingEnabled);

    }//GEN-LAST:event_btnGuardarActionPerformed

